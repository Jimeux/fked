create table if not exists user (
  id      varchar(15) primary key,
  created datetime not null,
  updated datetime not null default now()
);

create table if not exists reaction (
  code       varchar(50) primary key,
  fked_level tinyint not null
);

create table if not exists fked (
  user_id       varchar(15) not null references user (id),
  reaction_code varchar(50) not null references reaction (code),
  created       datetime    not null,
  primary key (user_id, reaction_code)
);

